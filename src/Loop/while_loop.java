package Loop;

public class while_loop {
	public static void main(String[] args) {     //print 1 to 10 num
		
		int i=1;                      //start num
		
		while(i<=10)                       //end num
		{
			System.out.println(i);
			
			i++;                        //incr or decr=incr by 1
			
		}
		
	}
	
	

}
