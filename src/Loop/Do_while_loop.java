package Loop;

public class Do_while_loop {
	public static void main(String[] args) {
		
		int i=1;                                   //start num
		
		do                                       //do loop
		{
			System.out.println(i);
			
			i++;                              //incr or decr=incr by 1
		}
		
		while(i<=10);                       //end num
		
	}

}
