package Loop;

public class For_loop_num_cube {
	public static void main(String[] args) {
		                                        //cube of 1 to 10 num
		for(int i=1;i<=10;i++)                  //start num=1
		{                                       //end num=10
			System.out.println(i*i*i);          //incr or decr=incr by 1
		}
	}

}
