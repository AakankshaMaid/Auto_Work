package Akki;

public class Swap_number_with_variable {
	public static void main(String[] args) {
		int a=30;
		int b=40;
		int c;
		
		System.out.println("Befor swaping:a="+a+" b="+b);
		c=a; //30
		a=b; //40
		b=c; //30
		
		System.out.println("After swaping:a="+a+" b="+b);
		
	}

}
