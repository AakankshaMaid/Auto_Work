package Akki;

public class Infinite_Loop {
	public static void main(String[] args) {
	
		// an infinite loop runs without any condition and runs infinietly 
		for(;;)  
		{
			System.out.println("hello");  //statement to execute
			
			break;   //if we want to brake the loop add breaking condition in loop body
			
		}
	}

}
