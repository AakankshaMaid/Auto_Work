package Logical_programs;

public class Compair_two_numbers {
	public static void main(String[]arg)
	{
		int A1[]= {10,15,60,90,80};
		int A2[]= {14,15,70,90,60};
		
		for(int i=0;i<=4;i++)
		{
			if(A1[i]==A2[i])
			{
				System.out.println(A1[i]+" "+A2[i]);
			}
		}
		
		
		
	}

}
