package Logical_programs;

public class number_swaping_with_variable
{
	public static void main(String[]arg)
	{
		int a=10;
		int b=20;
		int c;
		
		System.out.println("befor swaping a="+a+" b="+b);
		
		c=a;
		a=b;
		b=c;
		System.out.println("after swaping a="+a+" b="+b);
	}

}
