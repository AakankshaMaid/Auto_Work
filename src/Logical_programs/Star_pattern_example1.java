package Logical_programs;

public class Star_pattern_example1 
{
	public static void main(String[] args) {
		//***
		//***
		
		for(int i=1;i<=2;i++)  //outer for loop for rows
		{
			for(int j=1;j<=3;j++)  //inner for loop for colums
			{
			System.out.print("*");
			}
			System.out.println(" ");
		}
		
	}

}
