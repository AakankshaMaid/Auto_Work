package variable;

public class Demo1 {
	public static void main(String[] args) {
		String stdname;
		int std;               //variable declaration
		char div;
		float marks;
		
		stdname="vaibhav";      //variable initialization
		std=6;
		div='A';
		marks=60.20f;
		
		System.out.println("name of student = "+stdname);   //variable use
		System.out.println("standard = "+std);
		System.out.println("division = "+div);
		System.out.println("marks = "+marks);
	}

}
