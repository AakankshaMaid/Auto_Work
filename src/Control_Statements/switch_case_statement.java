package Control_Statements;

public class switch_case_statement {
	public static void main(String[] args) {
		
		switch(7)
		{
		case 1:System.out.println("sun");
		break;
		case 2:System.out.println("mon");
		break;
		case 3:System.out.println("tues");
		break;
		case 4:System.out.println("wed");
		break;
		case 5:System.out.println("thir");
		break;
		case 6:System.out.println("fri");
		case 7:System.out.println("sat");
		break;
		default:System.out.println("input is wrong");
			
		}
	}

}
