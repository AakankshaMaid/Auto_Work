package Casting;

public class primitive_casting1 
{   //1. implicit casting = converting lower data type info into higher data type---> widening
	int a=5;
	
	long b=a;
 
	//2. explicit casting = converting higher data type into lower data type--->narrowing
	double c=2.5;
	
	float d=(float)c;
	
	//3. boolean casting 
	
	boolean str=true;
}
